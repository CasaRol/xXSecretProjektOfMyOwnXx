package ohmshjulcalculator;

/**
 *
 * @author Alexa
 */
public class Unit {
    
    private double watt;
    private double amp;
    private double volt;
    private double ohm;

    public Unit() {
        this.watt = watt;
        this.amp = amp;
        this.volt = volt;
        this.ohm = ohm;
    }

    public double getWatt() {
        return watt;
    }

    public void setWatt(double watt) {
        this.watt = watt;
    }

    public double getAmp() {
        return amp;
    }

    public void setAmp(double amp) {
        this.amp = amp;
    }

    public double getVolt() {
        return volt;
    }

    public void setVolt(double volt) {
        this.volt = volt;
    }

    public double getOhm() {
        return ohm;
    }

    public void setOhm(double ohm) {
        this.ohm = ohm;
    }

    

    

    
}
